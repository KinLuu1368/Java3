    private void ok() {
        StringBuilder string = new StringBuilder();
        if (kiemTra()) {
            string.append("Ho ten: ").append(txtHo.getText()).append(" ").append(txtTen.getText());
            string.append("\nBi danh: ").append(txtBiDanh.getText());
            
            if (btnNam.isSelected()) {
                string.append("\nGioi tinh: Nam");
            } else if(btnNu.isSelected()) {
                string.append("\nGioi tinh: Nu");
            } else {
                string.append("\nGioi tinh: Khong xac dinh");
            }
            
            string.append("\nQue quan: ").append(cbbQue.getSelectedItem());
            
            if(chkAnchoi.isSelected()) {
                string.append("\nSo thich: An choi");
            }
            if(chkNgu.isSelected()) {
                string.append("\nSo thich: Ngu nghi");
            }
            if(chkNhay.isSelected()) {
                string.append("\nSo thich: Nhay");
            }
            if(!(txtNote.getText().equals(""))) {
                string.append("\nNote: ").append( txtNote.getText());
            }
            JOptionPane.showMessageDialog(this, string.toString());
        }
    }
    

    
