     private boolean kiemTra() {
        if(txtHo.getText().trim().equals("")) {
            JOptionPane.showMessageDialog(this, "Họ không được trống");
            txtHo.requestFocus();
            return false;
        } 
        if(txtTen.getText().trim().equals("")) {
            JOptionPane.showMessageDialog(this, "Tên không được trống");
            txtTen.requestFocus();
            return false;
        } 
        if(txtBiDanh.getText().trim().equals("")) {
            JOptionPane.showMessageDialog(this, "Bí danh không được trống");
            txtBiDanh.requestFocus();
            return false;
        } 
        if(txtPass.getPassword().equals("")) {
            JOptionPane.showMessageDialog(this, "Tên không được trống");
            txtTen.requestFocus();
            return false;
        } 
        if(!(chkAnchoi.isSelected()) && !(chkNgu.isSelected()) && !(chkNhay.isSelected())) {
            JOptionPane.showMessageDialog(this, "Chọn ít nhất 1 sở thích");
            return false;
        }
        if(!(btnNam.isSelected()) && !(btnNu.isSelected()) && !(btnKoXD.isSelected())) {
            JOptionPane.showMessageDialog(this, "Vui lòng chọn giới tính");
            return false;
        }
        return true;
    }
     
    

